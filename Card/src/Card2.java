
public class Card2 {
	
	String shape;
	String num;
	
	public Card2() {
	}
	
	public Card2(String shape, String num) {
		super();
		this.shape = shape;
		this.num = num;
	}
	
	public String getShape() {
		return shape;
	}
	
	public void setShape(String shape) {
		this.shape = shape;
	}
	
	public String getNum() {
		return num;
	}
	
	public void setNum(String num) {
		this.num = num;
	}
	
	
	
	
	
	
}
