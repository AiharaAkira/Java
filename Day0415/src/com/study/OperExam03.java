package com.study;

public class OperExam03 {
	public static void main(String[] args) {

		/* 비교연산:>,<,<=,>= 
		 * 항등관계연산: == ,!=
		 * */
		
		int i = 10;
		int j = 20;
		
		boolean c = i < j; // 변수 i 가 j보다 작다.
		System.out.println("i<j="+c);
		System.out.println();
		c = i != j; //변수 i와 변수 j는 같지 않다.
		System.out.println("i!=j="+c);
		
		
		
	
	}
}
