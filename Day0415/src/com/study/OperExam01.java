package com.study;

public class OperExam01 {
	public static void main(String[] args) {

		
		int i = 7,j = 5;
		
		// +,-,*,/,%
		System.out.println(i+"+"+j+"="+i+j);
		System.out.println(i+"-"+j+"="+(i-j));
		System.out.println(i+"*"+j+"="+i*j);
		System.out.println(i+"/"+j+"="+i/j);
		System.out.println(i+"%"+j+"="+i%j);
		System.out.printf("%d%%%d=%d\n",i,j,i%j);
		
		
		
		
		
	}
}
