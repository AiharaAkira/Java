package com.card;

public class Card {
	
	String shape;
	String num;
	
	
	public Card() {
		
	}


	public String getShape() {
		return shape;
	}


	public void setShape(String shape) {
		this.shape = shape;
	}


	public Card(String shape, String num) {
		super();
		this.shape = shape;
		this.num = num;
	}


	public String getNum() {
		return num;
	}


	public void setNum(String num) {
		this.num = num;
	}

	

	











	
	
}
